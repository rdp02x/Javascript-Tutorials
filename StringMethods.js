const text = "SpIdErMaN";

console.log(text.length); // Prints the lenght of the String
console.log(text.charAt(2)); // Prints the Char at give index [ at() ]
console.log(text.charCodeAt(2)); // Prints the ASCII value of the Char at give index
console.log(text.toUpperCase()); // Prints all the characters in Upper Case
console.log(text.toLowerCase()); // Prints all the characters in Lower Case

console.log(text.slice(0, 6)); // String Slicing allows us to cut the string in out desired lenght and number of characters
